# Arrays
cars = ["Ford", "Volvo", "BMW"]
print(type(cars))

# Access the Elements of an Array

# Get the value of the first array item:
x = cars[0]
print(x)

# Modify the value of the first array item:
x = cars
cars[0] = "Toyota"
print(x)

# The Length of an Array
print(len(x))

# Looping Array Elements
for x in cars:
    print(x)

